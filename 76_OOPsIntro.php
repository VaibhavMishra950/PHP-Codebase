    <h1>OOPs: Object Oriented Programming Style</h1>
<hr>
    <h2>What is OOPs?</h2>
    <p>OOPs is about creating classes and objects. Class serves as a template and multiple
    objects can be created using a class.</p>
<hr>
    <h2>What are Classes and Objects?</h2>
    <p>Classes are templates for creating objects</p>
    <p>If `car` is a class then `Ford Mustang GT` and `Mahindra Thar` are Objects of
    this class </p>
<hr>
    <h2>Why OOPs?</h2>
    <p>OOPs makes it easy to keep the code `DRY`</p>
    <p>DRY: Don't Repeat Yourself - the code written by the programmer should be reusable.</p>

